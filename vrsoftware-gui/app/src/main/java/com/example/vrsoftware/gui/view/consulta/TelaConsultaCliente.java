    private void carregarListaClientes() {
        DefaultTableModel model = (DefaultTableModel) jTableClientes.getModel();
        // TODO: Revisar tratamento de exceções
        try {
            ApiClient apiClient = new ApiClient("config.properties");
            Gson gson = new Gson();
            ClienteController clienteController = new ClienteController(apiClient, gson);

            String jsonResponse = clienteController.buscarTodosClientes();

            Cliente[] clientes = gson.fromJson(jsonResponse, Cliente[].class);

            // Limpa os dados existentes
            model.setRowCount(0);

            for (Cliente cliente : clientes) {
                Object[] dado = {
                    cliente.getCodigo(),
                    cliente.getNome(),
                    "R$ " + cliente.getLimiteCompra().setScale(2).toString(),
                    cliente.getDiaFechamento()
                };
                model.addRow(dado);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar dados: " + e.getMessage());
        }
    }

